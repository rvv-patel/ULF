const CompanyModel = require('../models/companyModel');

exports.getAll = async (req, res) => {
    try {
        const companies = await CompanyModel.getAll();
        res.json(companies);
    } catch (error) {
        console.error('Error in CompanyController.getAll:', error);
        res.status(500).json({ error: error.message });
    }
};

exports.getById = async (req, res) => {
    try {
        const id = parseInt(req.params.id);
        const item = await CompanyModel.getById(id);
        if (!item) return res.status(404).json({ message: 'Not found' });
        res.json(item);
    } catch (error) {
        console.error('Error in CompanyController.getById:', error);
        res.status(500).json({ error: error.message });
    }
};

exports.create = async (req, res) => {
    try {
        // ID is auto-generated by DB (SERIAL)
        const newItem = await CompanyModel.create({
            name: req.body.name,
            address: req.body.address || '',
            emails: req.body.emails || []
        });
        res.status(201).json(newItem);
    } catch (error) {
        console.error('Error in CompanyController.create:', error);
        res.status(500).json({ error: error.message });
    }
};

exports.update = async (req, res) => {
    try {
        const id = parseInt(req.params.id);
        // Check if exists
        const existing = await CompanyModel.getById(id);
        if (!existing) return res.status(404).json({ message: 'Not found' });

        const updatedItem = await CompanyModel.update(id, {
            name: req.body.name,
            address: req.body.address || '',
            emails: req.body.emails || []
        });
        res.json(updatedItem);
    } catch (error) {
        console.error('Error in CompanyController.update:', error);
        res.status(500).json({ error: error.message });
    }
};

exports.delete = async (req, res) => {
    try {
        const id = parseInt(req.params.id);
        const deletedItem = await CompanyModel.delete(id);
        if (!deletedItem) return res.status(404).json({ message: 'Not found' });

        res.json({ message: 'Deleted successfully' });
    } catch (error) {
        console.error('Error in CompanyController.delete:', error);
        res.status(500).json({ error: error.message });
    }
};

